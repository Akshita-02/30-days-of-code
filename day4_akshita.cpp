//to check whether number is even or odd:

#include <iostream>

using namespace std;

int main()
{
    int n;
    cout<<"Enter the number:"<<endl;
    cin>>n;
    if(n%2==0)
        cout<<"The number which you entered is even"<<endl;
        else
        cout<<"The number which you entered is odd"<<endl;

    return 0;
}